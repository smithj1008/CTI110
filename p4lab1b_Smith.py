    #Write a turtle graphics programs that draws a triangle and a square using loops.
import turtle


t = turtle.Turtle()


t.penup()
t.goto(0, 80)
t.pendown()


t.setheading(270)
t.forward(150)
t.circle(-50, 180)




t.penup()
t.goto(50, 100)
t.pendown()

t.forward(30)
t.backward(30)

t.circle(-90,-185)
t.circle(90,-250)
